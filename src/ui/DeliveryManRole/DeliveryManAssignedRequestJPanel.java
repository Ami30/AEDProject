    public void populateOrderTable() {
        DefaultTableModel model = (DefaultTableModel) completeOrderTable.getModel();
        model.setRowCount(0);
           for(OrderMedicine om : delman.getOrderDirectory().getMedicineList()){
            Object[] row = new Object[5];
            row[0] = om;
            row[1] = om.getUser().getName();
            row[2] = om.getPharmacyName();
            row[3] = om.getOrderDate();
            row[4] = om.getStatus();
            model.addRow(row);
        }
   
    }
