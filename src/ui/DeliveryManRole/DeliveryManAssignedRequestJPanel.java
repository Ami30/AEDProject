    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = completeOrderTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!");
            return;
        }
        else{
            OrderMedicine ordMed = (OrderMedicine)completeOrderTable.getValueAt(selectedRow, 0);
            OrderDetailsDelmanJPanel orderDetails=new OrderDetailsDelmanJPanel(userProcessContainer,ent,userAccount,system,organization, ordMed);
            userProcessContainer.add("PatientManagerProfileJPanel", orderDetails);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void ApprovedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApprovedActionPerformed
        // TODO add your handling code here:
        int row = completeOrderTable.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        OrderMedicine serReq=(OrderMedicine)completeOrderTable.getValueAt(row, 0);
        if(serReq.getStatus().equalsIgnoreCase("On the way") || serReq.getStatus().equalsIgnoreCase("completed") || serReq.getStatus().equalsIgnoreCase("Declined") || serReq.getStatus().equalsIgnoreCase("cancelled")){
            JOptionPane.showMessageDialog(null, "Order already marked "+ serReq.getStatus());
        }else{
            serReq.setStatus("On the way");
            JOptionPane.showMessageDialog(null, "Order marked on the way");
            populateOrderTable();
        }

    }//GEN-LAST:event_ApprovedActionPerformed

