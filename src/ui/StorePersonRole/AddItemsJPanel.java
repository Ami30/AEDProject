    private void populateTestTable(){
        DefaultTableModel model = (DefaultTableModel) itemsTable.getModel();
        
        model.setRowCount(0);
        for (Items item : storePerson.getItemsDirectory().getItemsList()){          
            Object[] row = new Object[2];
            row[0] = item;
            row[1] = item.getItemQuantity();
            model.addRow(row);
        }
    }
